    </el-row>
    <!-- 删除记录对话框 -->
    <el-dialog title="删除记录" :visible.sync="dialogVisibledele" width="80%">
      <el-table :data="tableData" style="width: 100%" max-height="250">
        <el-table-column
          fixed
          prop="createTime"
          key="createTime"
          label="日期"
          width="150"
        >
        </el-table-column>
        <el-table-column
